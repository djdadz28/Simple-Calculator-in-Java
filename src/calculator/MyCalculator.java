    private void equalsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalsButtonActionPerformed
        if(!"".equals(number_input)){
            try {
                if(!hasPreviousResult){
                    result = "" + engine.eval(number_input);
                    displayLabel.setText(result.substring(0,result.length() < 15 ? result.length() : 15));
                    previous_result = result.substring(0,result.length() < 15 ? result.length() : 15);
                    hasPreviousResult = true;
                    result = "";
                    number_input = "";
                }else{
                    result = "" + engine.eval(previous_result + number_input);
                    displayLabel.setText(result.substring(0,result.length() < 15 ? result.length() : 15));
                    previous_result = result.substring(0,result.length() < 15 ? result.length() : 15);
                    result = "";
                    number_input = "";
                }
                
            } catch (ScriptException ex) {
                System.out.println(ex);
            }
        }
        allow_input = false;
    }//GEN-LAST:event_equalsButtonActionPerformed

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        if(number_input.length() > 0){
        number_input = number_input.substring(0, number_input.length() - 1);
        displayLabel.setText(previous_result + number_input);
        }
    }//GEN-LAST:event_backButtonActionPerformed

