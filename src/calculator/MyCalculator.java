    void numberInputs(String num){
        if(!hasPreviousResult){
            number_input += num;
            displayLabel.setText(number_input);
        }else{
            if("+-*/".contains(num) || allow_input){
                allow_input = true;
                number_input += num;
                displayLabel.setText(previous_result + number_input);
            }
        }
    }
    
    
